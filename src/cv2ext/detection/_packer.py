class AbstractFramePacker(ABC):
    """
    Pack regions of a frame together based on detection activity.

    Detections are represented as a list of bounding boxes with
    scores and class id labels optional.
    """

    @abstractmethod
    def pack(
        self: Self,
        image: np.ndarray,
        exclude: tuple[int, int, int, int] | list[tuple[int, int, int, int]],
        regions: int = 1,
    ) -> (
        tuple[np.ndarray, np.ndarray]
        | tuple[list[np.ndarray], list[np.ndarray]]
    ):
        """
        Pack regions of a frame together.

        Parameters
        ----------
        image : np.ndarray
            The image to pack.
        exclude : tuple[int, int, int, int] | list[tuple[int, int, int, int]]
            Regions of the image to exclude from the packing.
        regions : int, optional
            Number of regions to distribute packed cells across.
            When regions=1 (default), returns single arrays for backward compatibility.
            When regions>1, returns lists of arrays.

        Returns
        -------
        tuple[np.ndarray, np.ndarray] | tuple[list[np.ndarray], list[np.ndarray]]
            When regions=1: (packed_image, transform)
            When regions>1: (list_of_images, list_of_transforms)

        """

    @abstractmethod
    def unpack(
        self: Self,
        detections: list[tuple[int, int, int, int]]
        | list[tuple[tuple[int, int, int, int], float, int]],
        transform: np.ndarray | list[np.ndarray],
        region_idx: int | None = None,
    ) -> (
        list[tuple[int, int, int, int]]
        | list[tuple[tuple[int, int, int, int], float, int]]
    ):
        """
        Unpack regions of a frame.

        Parameters
        ----------
        detections : list[tuple[int, int, int, int]] | list[tuple[tuple[int, int, int, int], float, int]]
            The regions to unpack.
        transform : np.ndarray | list[np.ndarray]
            The transform information generated by the pack method.
            When a list, requires region_idx to specify which region.
        region_idx : int, optional
            The region index when transform is a list.
            Required when transform is a list, ignored otherwise.

        Returns
        -------
        list[tuple[int, int, int, int]] | list[tuple[tuple[int, int, int, int], float, int]]

        """

    @abstractmethod
    def unpack_multi(
        self: Self,
        detections_per_region: list[
            list[tuple[int, int, int, int]]
            | list[tuple[tuple[int, int, int, int], float, int]]
        ],
        transforms: list[np.ndarray],
    ) -> (
        list[tuple[int, int, int, int]]
        | list[tuple[tuple[int, int, int, int], float, int]]
    ):
        """
        Unpack detections from multiple regions and combine results.

        Parameters
        ----------
        detections_per_region : list[list[...]]
            List of detections for each region.
        transforms : list[np.ndarray]
            List of transforms, one per region.

        Returns
        -------
        list[tuple[int, int, int, int]] | list[tuple[tuple[int, int, int, int], float, int]]
            Combined unpacked detections from all regions.

        """

    @abstractmethod
    def update(
        self: Self,
        detections: list[tuple[int, int, int, int]]
        | list[tuple[tuple[int, int, int, int], float, int]],
    ) -> None:
        """
        Update the packer with new detections.

        Parameters
        ----------
        detections : list[tuple[int, int, int, int]] | list[tuple[tuple[int, int, int, int], float, int]]
            The detections to update the packer with.

        """

    @abstractmethod
    def reset(
        self: Self,
        image_shape: tuple[int, int] | None = None,
        gridsize: int | None = None,
    ) -> None:
        """
        Reset the packer.

        Parameters
        ----------
        image_shape : tuple[int, int], optional
            The shape of the image in form (width, height).
        gridsize : int, optional
            The new gridsize value to use.

        """


@register_jit()
def _bbox_to_gridcells(
    bbox: tuple[int, int, int, int],
    row_step: int,
    col_step: int,
    n_rows: int,
    n_cols: int,
) -> list[tuple[int, int]]:
    x1, y1, x2, y2 = bbox
    min_row = max(0, math.floor(y1 / row_step))
    max_row = min(n_rows, math.ceil(y2 / row_step))
    min_col = max(0, math.floor(x1 / col_step))
    max_col = min(n_cols, math.ceil(x2 / col_step))

    cells: list[tuple[int, int]] = []
    for i in range(min_row, max_row):
        for j in range(min_col, max_col):
            cells.extend([(i, j)])
    return cells


@register_jit()
def _bboxes_to_gridcells(
    bboxes: list[tuple[int, int, int, int]],
    row_step: int,
    col_step: int,
    n_rows: int,
    n_cols: int,
) -> set[tuple[int, int]]:
    cells: list[tuple[int, int]] = []
    for bbox in bboxes:
        cells.extend(_bbox_to_gridcells(bbox, row_step, col_step, n_rows, n_cols))
    return set(cells)


@register_jit()
def _unpack_grid_single_bbox(
    bbox: tuple[int, int, int, int],
    transform: np.ndarray,
    gridsize: int,
) -> tuple[int, int, int, int]:
    x1, y1, x2, y2 = bbox

    # Determine grid coordinates for the packed bounding box
    n_col = int(((x1 + x2) / 2.0) // gridsize)
    n_row = int(((y1 + y2) / 2.0) // gridsize)

    # clamp indices
    n_row = max(0, min(n_row, transform.shape[0] - 1))
    n_col = max(0, min(n_col, transform.shape[1] - 1))

    # Retrieve original top-left offset for the grid cell
    o_x, o_y = transform[n_row][n_col]

    # Calculate the absolute coordinates in the original image
    abs_x1 = int(x1 - (n_col * gridsize) + o_x)
    abs_y1 = int(y1 - (n_row * gridsize) + o_y)
    abs_x2 = int(x2 - (n_col * gridsize) + o_x)
    abs_y2 = int(y2 - (n_row * gridsize) + o_y)

    return abs_x1, abs_y1, abs_x2, abs_y2


@register_jit()
def _unpack_grid_bboxes_conf_classid(
    detections: list[tuple[tuple[int, int, int, int], float, int]],
    transform: np.ndarray,
    gridsize: int,
) -> list[tuple[tuple[int, int, int, int], float, int]]:
    unpacked_dets: list[tuple[tuple[int, int, int, int], float, int]] = []
    for bbox, conf, classid in detections:
        abs_x1, abs_y1, abs_x2, abs_y2 = _unpack_grid_single_bbox(
            bbox,
            transform,
            gridsize,
        )

        # Append the unpacked detection
        unpacked_dets.append(((abs_x1, abs_y1, abs_x2, abs_y2), conf, classid))
    return unpacked_dets


@register_jit()
def _unpack_grid_bboxes(
    detections: list[tuple[int, int, int, int]],
    transform: np.ndarray,
    gridsize: int,
) -> list[tuple[int, int, int, int]]:
    unpacked_dets: list[tuple[int, int, int, int]] = []
    for bbox in detections:
        abs_x1, abs_y1, abs_x2, abs_y2 = _unpack_grid_single_bbox(
            bbox,
            transform,
            gridsize,
        )

        # Append the unpacked detection
        unpacked_dets.append((abs_x1, abs_y1, abs_x2, abs_y2))
    return unpacked_dets


def _distribute_cells_to_regions(
    cells: list[tuple[tuple[int, int, int, int], tuple[int, int]]],
    regions: int,
) -> list[list[tuple[tuple[int, int, int, int], tuple[int, int]]]]:
    """Distribute cells across regions using contiguous chunks."""
    if regions <= 1 or len(cells) == 0:
        return [cells]

    # Limit regions to number of cells
    regions = min(regions, len(cells))
    chunk_size = math.ceil(len(cells) / regions)

    return [cells[i : i + chunk_size] for i in range(0, len(cells), chunk_size)]


@register_jit()
def _simple_grid_repack_single(
    image: np.ndarray,
    cells: list[tuple[tuple[int, int, int, int], tuple[int, int]]],
    gridsize: int,
) -> tuple[np.ndarray, np.ndarray]:
    """Repack cells into a single image using simple grid layout."""
    # need to repack the cells into a new image
    num_cells = len(cells)
    dim1 = max(1, math.ceil(math.sqrt(num_cells)))
    dim2 = max(1, math.ceil(num_cells / dim1))

    # allocate new data for the patches
    new_image: np.ndarray = np.zeros(
        (dim2 * gridsize, dim1 * gridsize, 3),
        dtype=np.uint8,
    )

    # copy the old data into new packed image
    # generate the transforms
    new_grids: np.ndarray = np.zeros((dim2, dim1, 2), dtype=int)
    for i, (bbox, _) in enumerate(cells):
        x1, y1, x2, y2 = bbox

        # generate the new row/col
        n_row = math.floor(i / dim1)  # Fixed dimension calculation
        n_col = i % dim1

        # generate the new bounding box
        n_x1 = n_col * gridsize
        n_x2 = n_x1 + gridsize
        n_y1 = n_row * gridsize
        n_y2 = n_y1 + gridsize

        # generate the offset, same as old coords for x1, y1
        offset = (x1, y1)

        # perform the data copy
        new_image[n_y1:n_y2, n_x1:n_x2] = image[y1:y2, x1:x2]

        # save the new grid entry
        new_grids[n_row, n_col] = offset

    return new_image, new_grids


def _shelf_grid_repack_single(
    image: np.ndarray,
    cells: list[tuple[tuple[int, int, int, int], tuple[int, int]]],
    gridsize: int,
) -> tuple[np.ndarray, np.ndarray]:
    """Repack cells into a single image using shelf-based bin packing."""
    # two steps to the smart placement algorithm
    # STEP 1: identify dense groupings of cells
    # STEP 2: using shelf based 2d bin packing to repack

    # ======================
    # STEP 1: Grouping cells
    # ======================
    # Build cell grid and lookup table
    height, width = image.shape[:2]
    rows = math.ceil(height / gridsize)
    cols = math.ceil(width / gridsize)

    # Create boolean grid of active cells and bbox lookup
    cell_grid = np.zeros((rows, cols), dtype=np.bool_)
    lookup: list[list[tuple[int, int, int, int]]] = [
        [(0, 0, 0, 0) for _ in range(cols)] for _ in range(rows)
    ]
    for bbox, (r, c) in cells:
        cell_grid[r, c] = True
        lookup[r][c] = bbox

    # Optimized grouping using vectorized pattern detection
    groups = _build_groups_optimized(cell_grid)

    # ===============================
    # STEP 2: Repacking using shelves
    # ===============================
    # sort each sub group to maintain right then down (in image coords)
    # ordering such that simple iteratation will place
    # cells in correct spots later
    groups = [sorted(group, key=operator.itemgetter(0, 1)) for group in groups]

    # post process the groups to sort by overall size
    # sort in descending order so we greedily allocate
    # largest first
    groups.sort(key=len, reverse=True)

    # assign heuristic value to attempt to hold max dimensions to
    target_size = math.ceil(math.sqrt(len(cells)))

    # use shelf packing stragegies
    # should get fairly good fit
    # since we have 2 discrete shelf widths, focus on group to shelf fit gap
    # each entry is [group, height, width]
    shelves: list[tuple[list[tuple[int, int]], int, int]] = []

    # for each group, get first shelf where width fits
    for group in groups:
        # compute height/width of group based on corner cell
        corner = group[0]
        g_height = 1
        g_width = 1
        for cell in group:
            g_height = max(g_height, cell[0] - corner[0] + 1)
            g_width = max(g_width, cell[1] - corner[1] + 1)

        # special case where no shelves are allocated yet
        if len(shelves) == 0:
            shelves.append(
                (group, g_height, g_width),
            )
            continue

        # in this case there is at least one shelf
        # iterate over the shelves
        # case 1:
        # if we find a shelf with equal width to group
        # and the group fits below target_size in height
        # add the group to that shelf
        shelf_id = -1
        for s_idx, (_, s_height, s_width) in enumerate(shelves):
            if g_width != s_width:
                continue
            if g_height + s_height > target_size:
                continue

            # as soon as we find one, break
            shelf_id = s_idx
            break

        if shelf_id != -1:
            s_boxes, s_height, s_width = shelves[shelf_id]
            s_boxes.extend(group)
            s_height += g_height
            shelves[shelf_id] = (
                s_boxes,
                s_height,
                s_width,
            )
            continue

        # case 2:
        # could not find a shelf, need to make a new one
        shelves.append(
            (group, g_height, g_width),
        )

    # compute the overall dimensions of the new image and grid
    new_height = 0  # maximum height of any shelf
    new_width = 0  # sum of all shelf widths
    for _, s_height, s_width in shelves:
        new_height = max(new_height, s_height)
        new_width += s_width

    # create new image and new grid based on the new dimensions
    dim1 = max(new_height, 1)
    dim2 = max(new_width, 1)
    new_image: np.ndarray = np.zeros(
        (dim1 * gridsize, dim2 * gridsize, 3),
        dtype=np.uint8,
    )
    new_grid: np.ndarray = np.zeros((dim1, dim2, 2), dtype=int)

    # for each shelf, iterate over the cells and add them into grid and image
    # need to keep a rolling start width, to denote the offset of each shelf
    frontier = 0
    for s_boxes, s_height, s_width in shelves:
        # iterate over each (height, width) block in
        # the shelf. Place each cell in that spot and copy
        for i in range(s_height):
            for j in range(s_width):
                # compute box id based on s_width
                b_idx = i * s_width + j

                # compute the original coords from loc
                o_r, o_c = s_boxes[b_idx]
                x1, y1, x2, y2 = lookup[o_r][o_c]

                # based on the original image offset update new_Grid
                n_c = j + frontier
                new_grid[i][n_c] = (x1, y1)

                # update the region of new_image with the region of the original image
                n_y = i * gridsize
                n_x = n_c * gridsize
                new_image[n_y : n_y + gridsize, n_x : n_x + gridsize] = image[
                    y1:y2,
                    x1:x2,
                ]

        # once shelf is added, update the frontier (x coord tracker)
        frontier += s_width

    # finally return the completed new_image and transform info
    return new_image, new_grid


def _build_groups_optimized(
    cell_grid: np.ndarray,
) -> list[list[tuple[int, int]]]:
    """Build groups using vectorized pattern detection and direct group construction.

    Parameters
    ----------
    cell_grid : np.ndarray
        Boolean array of shape (rows, cols) indicating active cells.

    Returns
    -------
    list[list[tuple[int, int]]]
        List of groups, each group is a list of (row, col) coordinates.

    """
    rows, cols = cell_grid.shape
    used = np.zeros((rows, cols), dtype=np.bool_)
    groups: list[list[tuple[int, int]]] = []

    # Vectorized detection of 2x2 patterns (all 4 cells must be active)
    if rows > 1 and cols > 1:
        valid_2x2 = (
            cell_grid[:-1, :-1]
            & cell_grid[:-1, 1:]
            & cell_grid[1:, :-1]
            & cell_grid[1:, 1:]
        )
        # Find valid 2x2 top-left corners
        r_2x2, c_2x2 = np.where(valid_2x2)
        for i in range(len(r_2x2)):
            r, c = int(r_2x2[i]), int(c_2x2[i])
            if not (used[r, c] or used[r, c + 1] or used[r + 1, c] or used[r + 1, c + 1]):
                groups.append([(r, c), (r, c + 1), (r + 1, c), (r + 1, c + 1)])
                used[r, c] = True
                used[r, c + 1] = True
                used[r + 1, c] = True
                used[r + 1, c + 1] = True

    # Vectorized detection of 1x2 horizontal pairs
    if cols > 1:
        valid_1x2 = cell_grid[:, :-1] & cell_grid[:, 1:]
        r_1x2, c_1x2 = np.where(valid_1x2)
        for i in range(len(r_1x2)):
            r, c = int(r_1x2[i]), int(c_1x2[i])
            if not used[r, c] and not used[r, c + 1]:
                groups.append([(r, c), (r, c + 1)])
                used[r, c] = True
                used[r, c + 1] = True

    # Vectorized detection of 2x1 vertical pairs
    if rows > 1:
        valid_2x1 = cell_grid[:-1, :] & cell_grid[1:, :]
        r_2x1, c_2x1 = np.where(valid_2x1)
        for i in range(len(r_2x1)):
            r, c = int(r_2x1[i]), int(c_2x1[i])
            if not used[r, c] and not used[r + 1, c]:
                groups.append([(r, c), (r + 1, c)])
                used[r, c] = True
                used[r + 1, c] = True

    # Remaining singles
    r_single, c_single = np.where(cell_grid & ~used)
    for i in range(len(r_single)):
        groups.append([(int(r_single[i]), int(c_single[i]))])

    return groups


def _parallel_repack_regions(
    image: np.ndarray,
    cell_groups: list[list[tuple[tuple[int, int, int, int], tuple[int, int]]]],
    gridsize: int,
    repack_fn: Callable[
        [np.ndarray, list[tuple[tuple[int, int, int, int], tuple[int, int]]], int],
        tuple[np.ndarray, np.ndarray],
    ],
    executor: ThreadPoolExecutor,
) -> tuple[list[np.ndarray], list[np.ndarray]]:
    """Execute region repacking in parallel using provided thread pool."""
    num_regions = len(cell_groups)

    # Pre-allocate result lists to maintain ordering
    images: list[np.ndarray | None] = [None] * num_regions
    transforms: list[np.ndarray | None] = [None] * num_regions

    def process_region(
        idx: int,
        group: list[tuple[tuple[int, int, int, int], tuple[int, int]]],
    ) -> tuple[int, np.ndarray, np.ndarray]:
        if len(group) == 0:
            return (
                idx,
                np.zeros((gridsize, gridsize, 3), dtype=np.uint8),
                np.zeros((1, 1, 2), dtype=int),
            )
        img, trans = repack_fn(image, group, gridsize)
        return idx, img, trans

    futures = [
        executor.submit(process_region, i, g) for i, g in enumerate(cell_groups)
    ]
    for future in as_completed(futures):
        idx, img, trans = future.result()
        images[idx] = img
        transforms[idx] = trans

    return images, transforms  # type: ignore[return-value]


class AbstractGridFramePacker(AbstractFramePacker):
    """Pack regions of a frame together based on a grid."""

    def __init__(
        self: Self,
        image_shape: tuple[int, int],
        gridsize: int = 128,
        detection_buffer: int = 30,
        method: str = "shelf",
    ) -> None:
        """
        Create a new GridFramePacker.

        Parameters
        ----------
        image_shape : tuple[int, int]
            The shape of the image in form (width, height).
        gridsize : int, optional
            The size of each cell in the overlaid grid.
            Default is 128.
        detection_buffer : int, optional
            The number of frames to consider for detection activity.
            Used instead of current frame count once frame count exceeds buffer size.
            Allows more recent detections to have more influence.
            Default is 30.
        method : str, optional
            The method to use for repacking grid cells into new images.
            By default, 'shelf'
            Options are: ['simple', 'shelf']
            Simple will place tiles of the grid FCFS basis in the new image,
            while shelf will attempt to place connected regions together.

        """
        super().__init__()
        self._width, self._height = image_shape
        self._gridsize = gridsize
        self._detection_buffer = detection_buffer
        self._method = method

        # assign type hints to variables used in initialize_cells
        self._n_cols: int
        self._n_rows: int
        self._col_step: int
        self._row_step: int
        self._num_dets: np.ndarray
        self._cells: np.ndarray

        self._initialize_cells()

        # tracking variables
        self._counter: int = 0

        # executor for parallel region processing
        self._executor: ThreadPoolExecutor | None = None

    def reset(
        self: Self,
        image_shape: tuple[int, int] | None = None,
        gridsize: int | None = None,
    ) -> None:
        """
        Reset the packer.

        Parameters
        ----------
        image_shape : tuple[int, int], optional
            The shape of the image in form (width, height).
        gridsize : int, optional
            The new gridsize value to use.

        """
        if image_shape:
            self._width, self._height = image_shape
        if gridsize:
            self._gridsize = gridsize
        self._initialize_cells()
        self._counter = 0

    def _initialize_cells(self: Self) -> None:
        """Initialize the grid cells and related parameters."""
        # num rows/cols
        self._n_cols = math.ceil(self._width / self._gridsize)
        self._n_rows = math.ceil(self._height / self._gridsize)

        # step size between grid cells
        self._col_step = (
            int((self._width - self._gridsize) / (self._n_cols - 1))
            if self._n_cols > 1
            else self._width
        )
        self._row_step = (
            int((self._height - self._gridsize) / (self._n_rows - 1))
            if self._n_rows > 1
            else self._height
        )

        # detection count info
        self._num_dets = np.zeros((self._n_rows, self._n_cols), dtype=int)

        # create the cell setup
        self._cells = np.zeros((self._n_rows * self._n_cols, 6), dtype=int)
        index: int = 0
        for i in range(self._n_rows):
            for j in range(self._n_cols):
                v_off = i * self._row_step
                h_off = j * self._col_step
                bbox = (
                    h_off,
                    v_off,
                    h_off + self._gridsize,
                    v_off + self._gridsize,
                )
                self._cells[index, :4] = bbox
                self._cells[index, 4:] = (i, j)
                index += 1

    def _get_executor(self: Self, max_workers: int) -> ThreadPoolExecutor:
        """Get or create thread pool executor for parallel region processing."""
        if self._executor is None:
            self._executor = ThreadPoolExecutor(max_workers=max_workers)
        return self._executor

    def close(self: Self) -> None:
        """Release resources. Call when done using the packer."""
        if self._executor is not None:
            self._executor.shutdown(wait=False)
            self._executor = None

    @abstractmethod
    def _should_explore(
        self: Self,
        image: np.ndarray,
        bbox: tuple[int, int, int, int],
        row: int,
        col: int,
        detections: int,
    ) -> bool:
        """
        Whether to explore a grid cell based on detection activity.

        Parameters
        ----------
        image : np.ndarray
            The image to be packed.
        bbox : tuple[int, int, int, int]
            The bounding box of the cell.
        row : int
            The row of the cell.
        col : int
            The column of the cell.
        detections : int
            The number of detections in the cell.

        Returns
        -------
        bool
            Whether to explore the cell.

        """

    def _filter_cells_vectorized(
        self: Self,
        image: np.ndarray,  # noqa: ARG002
        included_cells: list[tuple[int, int, int, int, int, int]],
    ) -> list[tuple[tuple[int, int, int, int], tuple[int, int]]]:
        """
        Filter cells using vectorized operations.

        Default implementation falls back to per-cell _should_explore calls.
        Subclasses can override for better performance.

        Parameters
        ----------
        image : np.ndarray
            The image to be packed.
        included_cells : list[tuple[int, int, int, int, int, int]]
            List of (x1, y1, x2, y2, row, col) tuples for cells to filter.

        Returns
        -------
        list[tuple[tuple[int, int, int, int], tuple[int, int]]]
            Filtered cells as list of ((x1, y1, x2, y2), (row, col)).

        """
        filtered_cells: list[tuple[tuple[int, int, int, int], tuple[int, int]]] = []
        for x1, y1, x2, y2, row, col in included_cells:
            bbox = (x1, y1, x2, y2)
            if self._should_explore(
                image,
                bbox,
                row,
                col,
                self._num_dets[row][col],
            ):
                filtered_cells.append((bbox, (row, col)))
        return filtered_cells

    def pack(
        self,
        image: np.ndarray,
        exclude: tuple[int, int, int, int]
        | list[tuple[int, int, int, int]]
        | None = None,
        method: str | None = None,
        regions: int = 1,
    ) -> (
        tuple[np.ndarray, np.ndarray]
        | tuple[list[np.ndarray], list[np.ndarray]]
    ):
        """
        Pack regions of a frame together.

        Parameters
        ----------
        image : np.ndarray
            The image to pack.
        exclude : tuple[int, int, int, int] | list[tuple[int, int, int, int]], optional
            Regions of the image to exclude from the packing.
            By default None.
        method : str, optional
            The method to pack the bounding boxes with.
            By default, None
            Options are: ['simple', 'shelf']
        regions : int, optional
            Number of regions to distribute packed cells across.
            When regions=1 (default), returns single arrays for backward compatibility.
            When regions>1, returns lists of arrays.

        Returns
        -------
        tuple[np.ndarray, np.ndarray] | tuple[list[np.ndarray], list[np.ndarray]]
            When regions=1: (packed_image, transform)
            When regions>1: (list_of_images, list_of_transforms)

        Raises
        ------
        ValueError
            If the image shape does not match the packer shape.
            If regions < 1.

        """
        if regions < 1:
            err_msg = f"regions must be >= 1, got {regions}"
            raise ValueError(err_msg)

        height, width = image.shape[:2]
        if height != self._height or width != self._width:
            err_msg = f"Image shape {image.shape} does not match packer shape {self._height, self._width}."
            raise ValueError(err_msg)

        # get the excluded cells
        excluded_cells: set[tuple[int, int]] = set()
        if exclude is not None and len(exclude) > 0:
            if isinstance(exclude, tuple):
                excluded_cells = set(
                    _bbox_to_gridcells(
                        exclude,
                        self._row_step,
                        self._col_step,
                        self._n_rows,
                        self._n_cols,
                    ),
                )
            else:
                excluded_cells = _bboxes_to_gridcells(
                    exclude,
                    self._row_step,
                    self._col_step,
                    self._n_rows,
                    self._n_cols,
                )

        # get all cells which are not in the excluded region
        included_cells = [
            (x1, y1, x2, y2, r, c)
            for (x1, y1, x2, y2, r, c) in self._cells
            if (r, c) not in excluded_cells
        ]

        # filter cells using vectorized operations when possible
        filtered_cells = self._filter_cells_vectorized(image, included_cells)

        # use the simple grid repacking
        method = method or self._method

        # Fast path for single region - bypass wrapper functions
        if regions == 1:
            if method == "simple":
                packed_image, transform = _simple_grid_repack_single(
                    image,
                    filtered_cells,
                    self._gridsize,
                )
            else:
                packed_image, transform = _shelf_grid_repack_single(
                    image,
                    filtered_cells,
                    self._gridsize,
                )
            # update the image
            self._prev_image = image
            self._counter += 1
            return packed_image, transform

        # Multi-region path - parallel processing
        cell_groups = _distribute_cells_to_regions(filtered_cells, regions)
        executor = self._get_executor(max_workers=regions)

        if method == "simple":
            images, transforms = _parallel_repack_regions(
                image,
                cell_groups,
                self._gridsize,
                _simple_grid_repack_single,
                executor,
            )
        else:
            images, transforms = _parallel_repack_regions(
                image,
                cell_groups,
                self._gridsize,
                _shelf_grid_repack_single,
                executor,
            )

        # update the image
        self._prev_image = image
        self._counter += 1
        return images, transforms

    def unpack(
        self: Self,
        detections: list[tuple[int, int, int, int]]
        | list[tuple[tuple[int, int, int, int], float, int]],
        transform: np.ndarray | list[np.ndarray],
        region_idx: int | None = None,
    ) -> (
        list[tuple[int, int, int, int]]
        | list[tuple[tuple[int, int, int, int], float, int]]
    ):
        """
        Unpack regions of a frame.

        Parameters
        ----------
        detections : list[tuple[int, int, int, int]] | list[tuple[tuple[int, int, int, int], float, int]]
            The regions to unpack.
        transform : np.ndarray | list[np.ndarray]
            The transform information generated by the pack method.
            When a list, requires region_idx to specify which region.
        region_idx : int, optional
            The region index when transform is a list.
            Required when transform is a list, ignored otherwise.

        Returns
        -------
        list[tuple[int, int, int, int]] | list[tuple[tuple[int, int, int, int], float, int]]

        Raises
        ------
        ValueError
            If transform is a list but region_idx is not provided.
        IndexError
            If region_idx is out of bounds.

        """
        if len(detections) == 0:
            return []

        # Handle list of transforms (multi-region case)
        if isinstance(transform, list):
            if region_idx is None:
                err_msg = "region_idx is required when transform is a list"
                raise ValueError(err_msg)
            transform = transform[region_idx]

        # get the type of detections passed
        if len(detections[0]) == 3:
            return _unpack_grid_bboxes_conf_classid(
                detections,  # type: ignore[arg-type]
                transform,
                self._gridsize,
            )

        return _unpack_grid_bboxes(detections, transform, self._gridsize)  # type: ignore[arg-type]

    def unpack_multi(
        self: Self,
        detections_per_region: list[
            list[tuple[int, int, int, int]]
            | list[tuple[tuple[int, int, int, int], float, int]]
        ],
        transforms: list[np.ndarray],
    ) -> (
        list[tuple[int, int, int, int]]
        | list[tuple[tuple[int, int, int, int], float, int]]
    ):
        """
        Unpack detections from multiple regions and combine results.

        Parameters
        ----------
        detections_per_region : list[list[...]]
            List of detections for each region.
        transforms : list[np.ndarray]
            List of transforms, one per region.

        Returns
        -------
        list[tuple[int, int, int, int]] | list[tuple[tuple[int, int, int, int], float, int]]
            Combined unpacked detections from all regions.

        Raises
        ------
        ValueError
            If length of detections_per_region doesn't match transforms.

        """
        if len(detections_per_region) != len(transforms):
            err_msg = (
                f"Length mismatch: {len(detections_per_region)} detection lists "
                f"vs {len(transforms)} transforms"
            )
            raise ValueError(err_msg)

        all_unpacked: list = []
        for region_idx, (dets, trans) in enumerate(
            zip(detections_per_region, transforms)
        ):
            unpacked = self.unpack(dets, trans, region_idx=None)
            all_unpacked.extend(unpacked)

        return all_unpacked

    def update(
        self: Self,
        detections: list[tuple[int, int, int, int]]
        | list[tuple[tuple[int, int, int, int], float, int]],
    ) -> None:
        """
        Update the packer with new detections.

        Parameters
        ----------
        detections : list[tuple[int, int, int, int]] | list[tuple[tuple[int, int, int, int], float, int]]
            The detections to update the packer with.

        """
        # add det counts to all possible grids with detections
        for elements in detections:
            if len(elements) == 3:
                bbox, _, _ = elements
            else:
                bbox = elements

            # get the grid cells that the bbox intersects
            grids = _bbox_to_gridcells(
                bbox,
                self._row_step,
                self._col_step,
                self._n_rows,
                self._n_cols,
            )

            # increment the detection count for each grid cell
            for o_row, o_col in grids:
                if 0 <= o_row < self._n_rows and 0 <= o_col < self._n_cols:
                    self._num_dets[o_row][o_col] += 1

        # decrement all counters by 1
        self._num_dets = np.maximum(0, self._num_dets - 1)


class AnnealingFramePacker(AbstractGridFramePacker):
    """
    Pack regions of a frame together based on detection activity.

    Detections are represented as a list of bounding boxes with
    scores and class id labels optional.
    """

    def __init__(
        self: Self,
        image_shape: tuple[int, int],
        gridsize: int = 128,
        alpha: float = 0.01,
        min_prob: float = 0.1,
        detection_buffer: int = 30,
        method: str = "shelf",
    ) -> None:
        """
        Create a new AnnealingFramePacker.

        Parameters
        ----------
        image_shape : tuple[int, int]
            The shape of the image in form (width, height).
        gridsize : int, optional
            The size of each cell in the overlaid grid.
            Default is 128.
        alpha : float, optional
            The learning rate for the annealing process.
            Default is 0.01.
        min_prob : float, optional
            The minimum probability for a region to be considered active.
            Default is 0.1.
        detection_buffer : int, optional
            The number of frames to consider for detection activity.
            Used instead of current frame count once frame count exceeds buffer size.
            Allows more recent detections to have more influence.
            Default is 30.
        method : str, optional
            The method to use for repacking grid cells into new images.
            By default, 'shelf'
            Options are: ['simple', 'shelf']
            Simple will place tiles of the grid FCFS basis in the new image,
            while shelf will attempt to place connected regions together.

        """
        super().__init__(image_shape, gridsize, detection_buffer, method)

        # specific annealing parameters
        self._alpha = alpha
        self._min_prob = min_prob

    def _should_explore(
        self: Self,
        image: np.ndarray,  # noqa: ARG002
        bbox: tuple[int, int, int, int],  # noqa: ARG002
        row: int,  # noqa: ARG002
        col: int,  # noqa: ARG002
        detections: int,
    ) -> bool:
        time_factor = math.exp(-self._alpha * self._counter)
        detection_factor = min(
            1.0,
            detections / (min(self._counter, self._detection_buffer - 1) + 1),
        )
        explore_probability = max(self._min_prob, time_factor + detection_factor)
        return random.random() < explore_probability

    def _filter_cells_vectorized(
        self: Self,
        image: np.ndarray,  # noqa: ARG002
        included_cells: list[tuple[int, int, int, int, int, int]],
    ) -> list[tuple[tuple[int, int, int, int], tuple[int, int]]]:
        """Filter cells using vectorized NumPy operations."""
        if len(included_cells) == 0:
            return []

        # Convert to numpy array for vectorized operations
        cells_arr = np.array(included_cells, dtype=np.int32)
        rows = cells_arr[:, 4]
        cols = cells_arr[:, 5]

        # Get detection counts for all cells at once
        detections = self._num_dets[rows, cols].astype(np.float64)

        # Compute time_factor (constant for all cells)
        time_factor = math.exp(-self._alpha * self._counter)

        # Compute detection_factor vectorized
        divisor = min(self._counter, self._detection_buffer - 1) + 1
        detection_factor = np.minimum(1.0, detections / divisor)

        # Compute explore_probability vectorized
        explore_probability = np.maximum(self._min_prob, time_factor + detection_factor)

        # Generate all random values at once
        random_values = np.random.random(len(included_cells))

        # Boolean mask for cells to explore
        mask = random_values < explore_probability

        # Build result list from masked cells
        filtered_cells: list[tuple[tuple[int, int, int, int], tuple[int, int]]] = []
        for idx in np.where(mask)[0]:
            x1, y1, x2, y2, row, col = included_cells[idx]
            filtered_cells.append(((x1, y1, x2, y2), (row, col)))

        return filtered_cells


class RandomFramePacker(AbstractGridFramePacker):
    """Pack regions of a frame together randomly."""

    def __init__(
        self: Self,
        image_shape: tuple[int, int],
        gridsize: int = 128,
        threshold: float = 0.1,
        detection_buffer: int = 30,
        method: str = "shelf",
    ) -> None:
        """
        Create a new RandomFramePacker.

        Parameters
        ----------
        image_shape : tuple[int, int]
            The shape of the image in form (width, height).
        gridsize : int, optional
            The size of each cell in the overlaid grid.
            Default is 128.
        threshold : float, optional
            The threshold for which to randomly explore a grid cell.
            If the random value is less than the threshold, the cell is explored.
            Threshold of 0.1 means 10% of the time a cell is explored.
            Default is 0.1.
        detection_buffer : int, optional
            The number of frames to consider for detection activity.
            Used instead of current frame count once frame count exceeds buffer size.
            Allows more recent detections to have more influence.
            Default is 30.
        method : str, optional
            The method to use for repacking grid cells into new images.
            By default, 'shelf'
            Options are: ['simple', 'shelf']
            Simple will place tiles of the grid FCFS basis in the new image,
            while shelf will attempt to place connected regions together.

        """
        super().__init__(image_shape, gridsize, detection_buffer, method)

        # specific parameters
        self._threshold = threshold

    def _should_explore(
        self: Self,
        image: np.ndarray,  # noqa: ARG002
        bbox: tuple[int, int, int, int],  # noqa: ARG002
        row: int,  # noqa: ARG002
        col: int,  # noqa: ARG002
        detections: int,  # noqa: ARG002
    ) -> bool:
        return random.random() < self._threshold

    def _filter_cells_vectorized(
        self: Self,
        image: np.ndarray,  # noqa: ARG002
        included_cells: list[tuple[int, int, int, int, int, int]],
    ) -> list[tuple[tuple[int, int, int, int], tuple[int, int]]]:
        """Filter cells using vectorized NumPy operations."""
        if len(included_cells) == 0:
            return []

        # Generate all random values at once
        random_values = np.random.random(len(included_cells))

        # Boolean mask for cells to explore
        mask = random_values < self._threshold

        # Build result list from masked cells
        filtered_cells: list[tuple[tuple[int, int, int, int], tuple[int, int]]] = []
        for idx in np.where(mask)[0]:
            x1, y1, x2, y2, row, col = included_cells[idx]
            filtered_cells.append(((x1, y1, x2, y2), (row, col)))

        return filtered_cells
